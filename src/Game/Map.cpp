Map::Map(int map[23][40]) :
	sf::Drawable()
{
	this->loadMap(map);
	this->loadAssets();
}

Map::~Map()
{}

void Map::loadMap(int map[23][40])
{
	// From
	// https://stackoverflow.com/questions/10274162/how-to-find-2d-array-size-in-c
	int rows = sizeof(map[0]) / sizeof(map[0]); // 23
	std::cout << rows << std::endl;

	int cols = sizeof(map[0]) / sizeof(int); // 1048
	std::cout << cols << std::endl;

	for (int y = 0; y < cols; y++)
	{
		for (int x = 0; x < rows; x++)
		{
			this->currentMap.insert(std::make_pair(map[x][y], sf::Vector2i(x, y)));
		}
	}

	//this->map = map;
	std::cout << map << std::endl;
}

void Map::loadAssets()
{
	sf::Texture tex;

	if (tex.loadFromFile("content/Tiles/Example.png"))
		throw "Could not load texture";

	this->tiles.push_back(sf::Sprite(tex));
}

void Map::draw(sf::RenderTarget& target, sf::RenderStates states) const
{
	std::map<int, sf::Vector2i>::iterator mapIt;
	for (mapIt == this->currentMap.begin(); mapIt != this->currentMap.end(); mapIt++)
	{
		target.draw(this->tiles.at(mapIt->first), states);
	}
}